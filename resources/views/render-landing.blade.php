Traffic Accident Prediction System for EDSA 
</h1>

<div class="max-w-md flex text-darkGrayishBlue mt-5">
This is an accident prediction system that is to be used for predicting possible traffic accidents within EDSA using our self-made prediction API; which uses multiple models and one main source of officially managed tweet source, that is MMDA's. The goal of this system is to help MMDA officials, public and private motorists, and commuters of any kind to be able to manage and plan their travel ahead of time to avoid traffic-related accidents within EDSA. With this system, we are hoping to reduce the number of traffic-related accidents within that area.
